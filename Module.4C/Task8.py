# Use a for loop to iterate from 0 to 10
for i in range(11):
    # Print out "Number" and the current value of i for each iteration
    print("Number", i)

    # If the value of i is equal to 2, continue to the next iteration
    if i == 2:
        continue

    # If the value of i is equal to 8, break out of the loop
    if i == 8:
        break
